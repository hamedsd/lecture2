i=2
j=3
print("i < j --> {0}".format(i<j))
print("i > j --> {0}".format(i>j))
print("i <= j --> {0}".format(i<=j))
print("i >= j --> {0}".format(i>=j))
print("i is equal to j --> {0}".format(i==j))
print("i is not equal to j --> {0}".format(i!=j))
